#include "libft.h"

void	*ft_memset(void *ch, int a, int len)
{
	int	i;

	i = 0;
	if (len > 0)
	{
		while (i < len)
		{
			((char *)ch)[i] = (char)a;
			i++;
		}
	}
	return (ch);
}
