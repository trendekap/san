#include "libft.h"

char	*ft_strchr(char *a, int b)
{
	int	len;

	len = 0;
	while (*a)
	{
		if (*a == (char)b)
			return (a);
		a++;
	}
	if (*a == (char)b)
		return (a);
	return ((void *)0);
}
