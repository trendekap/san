#include "libft.h"

void	*ft_memcpy(void *a, const void *b, int size)
{
	int	i;

	i = 0;
	if (a == b)
		return (a);
	while (i < size)
	{
		((char *)a)[i] = ((char *)b)[i];
		i++;
	}
	return (a);
}
