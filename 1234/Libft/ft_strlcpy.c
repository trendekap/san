#include "libft.h"

int	ft_strlcpy(char *a, const char *b, size_t size)
{
	size_t	i;

	i = 0;
	if (size > 0)
	{
		while (b[i] && (i < (size - 1)))
		{
			a[i] = b[i];
			i++;
		}
		a[i] = '\0';
	}
	return (ft_strlen(b));
}
