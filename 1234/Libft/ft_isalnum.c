#include "libft.h"

int	ft_isalnum(int ch)
{
	if ((ch <= '9' && ch >= '0') || (ch >= 'A' && ch <= 'Z')
		|| (ch <= 'z' && ch >= 'a'))
	{
		return (1);
	}
	return (0);
}
